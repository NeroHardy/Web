          El Ingenio San José de Abajo fue fundado en 1898 por el Sr. Clemente Cruz Pineiro sobre lo que eran las instalaciones de un trapiche. En los inicios del ingenio la compañía "Zaldo Hermanos" del Puerto de Veracruz, quien refacción de todo tipo de materiales y trabajos el ingenio (1898 - 1901) al no poder pagar las refacciones recibidas el Sr. Cruz Pineiro, entregó en venta el ingenio a la mencionada compañía.

Fueron varios administradores los que manejaron esta compañía, uno de estos fue el Sr. Juan Perdomo Medina (originario de la Isla Gran Canaria, en España) que ingresó a administrar el ingenio en el año de 1903. El Sr. Perdomo venía procedente de Cuba, en donde se había dedicado íntegramente al cultivo de la caña de azúcar.
En el año de 1923 ingresa al ingenio el Sr. Rodolfo Perdomo González, desarrollando su trabajo en el área de fábrica.

En sus inicios el ingenio tenía 534 hectáreas de siembra de caña de azúcar; sin embargo, en 1924 debido a la creación del Ejido Mata Clara le son expropiadas al ingenio 170 hectáreas de tierra, por lo que el área de cultivo quedó reducida a 361 hectáreas.
En esos años, el ingenio tenía una producción de azúcar en zafra normal de 100 a 150 toneladas, pero la reducción de tierras disminuyó notablemente la disponibilidad de materia prima.

En 1927 el Sr. Cristóbal Perdomo González responsable del área de campo, decidió asociarse con el Sr. Aniceto Celis del Ejido San José de En Medio, sembrando en sus parcelas cuatro hectáreas de caña de azúcar. Asimismo le proporcionaron a otros pequeños ejidatarios los recursos suficientes para la siembra de caña, con la finalidad de disponer de suficiente materia prima para alimentar el molino.
